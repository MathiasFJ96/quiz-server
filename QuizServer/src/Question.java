
public class Question {
	
	public String QuizText;
	public String A;
	public String B;
	public String C;
	public String D;
	public String Ans;
	
	public Question(String QuizText, String A, String B, String C, String D, String Ans) {
		this.QuizText = QuizText;
		this.A = A;
		this.B = B;
		this.C = C;
		this.D = D;
		this.Ans = Ans;
	}
}
